package forloops;
public class ForLoops {
public static void main(String[] args) {
        for(int i=0;i<=5;i++)
        {
            System.out.println(i);
        }
        for(int a=0;a<=10;a++)
        {
            System.out.println(a);
        }
        for(int b=10;b<=20;b++)
        {
            System.out.println(b);
        }
        for(int c=10;c>0;c--)
        {
            System.out.println(c);
        }
        
    }
    
}
