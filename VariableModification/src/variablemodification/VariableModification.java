
package variablemodification;

public class VariableModification {

    public static void main(String[] args) {
        int i=1;
        System.out.println(i);
        i=2;
        System.out.println(i);
        i=3;
        System.out.println(i);
        i=i+1; //Increment
        System.out.println(i);
        i++;//Increment...
        System.out.println(i);
        i--;//Decrement
        System.out.println(i);
        i=i-1;//Decrement
        System.out.println(i);
        i=i*2;
        System.out.println(i);
        i=i/3;
        System.out.println(i);
        
    }
    
}
